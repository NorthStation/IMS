package com.lms.zx.exception.sales;

public class SalesDaoException extends Exception {

	public SalesDaoException() {
		super();
	}

	public SalesDaoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SalesDaoException(String arg0) {
		super(arg0);
	}

	public SalesDaoException(Throwable arg0) {
		super(arg0);
	}

}
