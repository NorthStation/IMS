package com.lms.zx.exception.sales;

public class StockAmountIsLackedException extends Exception {

	public StockAmountIsLackedException() {
		super();
	}

	public StockAmountIsLackedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public StockAmountIsLackedException(String arg0) {
		super(arg0);
	}

	public StockAmountIsLackedException(Throwable arg0) {
		super(arg0);
	}

}
