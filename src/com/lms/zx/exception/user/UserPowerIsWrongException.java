package com.lms.zx.exception.user;

public class UserPowerIsWrongException extends Exception {

	public UserPowerIsWrongException() {
		super();
	}

	public UserPowerIsWrongException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UserPowerIsWrongException(String arg0) {
		super(arg0);
	}

	public UserPowerIsWrongException(Throwable arg0) {
		super(arg0);
	}

}
