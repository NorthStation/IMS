package com.lms.zx.exception.user;

public class UserPasswordIsWrongException extends Exception {

	public UserPasswordIsWrongException() {
		super();
	}

	public UserPasswordIsWrongException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UserPasswordIsWrongException(String arg0) {
		super(arg0);
	}

	public UserPasswordIsWrongException(Throwable arg0) {
		super(arg0);
	}

}
